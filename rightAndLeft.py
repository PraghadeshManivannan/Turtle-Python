import turtle

turtle = turtle.Turtle()

print(turtle.heading()) #gives the detail about where the turtle is pointing

#Turning right

#right
#turtle.right(angle) angle will be in either degree or radians

turtle.fd(100)
turtle.right(90)
print(turtle.heading())


#rt
#turtle.rt(angle) angle will be in either degree or radians

turtle.fd(100)
turtle.rt(90)
print(turtle.heading())


#Turning left
#left
#turtle.left(angle) angle will be in either degree or radians

turtle.fd(100)
turtle.left(90)
print(turtle.heading())


#lt
#turtle.lt(angle) angle will be in either degree or radians

turtle.fd(100)
turtle.lt(90)
print(turtle.heading())

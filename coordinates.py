import turtle

turtle = turtle.Turtle()

# turtle.xcor
#returns x:co-ordinate

turtle.lt(90)
turtle.fd(100)

print(turtle.pos())
print(round(turtle.xcor(),5))


# turtle.ycor
#returns y:co-ordinate

turtle.lt(90)
turtle.fd(100)

print(turtle.pos())

import turtle

turtle = turtle.Turtle()

print(turtle.position())

# Moving forward
#forward
#turtle.forward(distance) the distance will be in pixels

turtle.forward(100)
print(turtle.position())

#fd
#turtle.fd(distance) the distance will be in pixels

turtle.fd(100)
print(turtle.position())


#Moving backward
#backward
#turtle.backward(distance) the distance will be in pixels

turtle.backward(100)
print(turtle.position())

#back
#turtle.back(distance) the distance will be in pixels

turtle.back(100)
print(turtle.position())

#bd
#turtle.bk(distance) the distance will be in pixels

turtle.bk(100)
print(turtle.position())
